#include "libft.h"

size_t		ft_fact(int num)
{
	int		i;
	size_t	res;

	if (num == 0)
		return (1);
	res = 1;
	i = 1;
	while (i < num)
	{
		res = res * (i + 1);
		i++;
	}
	return (res);
}
