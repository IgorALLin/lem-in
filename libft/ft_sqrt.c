#include "libft.h"

int		ft_sqrt(int num)
{
	int i;

	if (num == 1)
		return (1);
	if (num == 4)
		return (2);
	i = 3;
	while (i <= num / 3)
	{
		if (i * i == num)
			return (i);
		i++;
	}
	return (0);
}
